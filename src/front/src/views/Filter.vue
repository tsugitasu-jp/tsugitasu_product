<script>
import FooterMenu from "../components/FooterMenu.vue";

export default {
  components: { 
    FooterMenu
  },
  data() {
    return {
      disp_list:{
        eval_materials:[],
        create_materials: [],
      },
    };
  },
  watch: {
    '$route': function (to, from) {
      if (to.query.tag !== from.query.tag) {
        this.init()
      }
    }
  },
  created() {
    this.init();
  },
  computed: {
  },
  methods: {
    init(){
      let tags = this.$route.query.tag
      if (tags){
        let txt = ''
        for (let i = 0; i< tags.length;i++){
          txt += '&'
          txt += ("tag=" + tags[i])
        }  
        this.axios
          .get('/api-v1/materials/?op='+'evaluation'+txt)
          .then((response) => {
            this.$set(this.disp_list, 'eval_materials', response.data)
          })
          .catch(function(error) {
              console.log(error)
          })
        this.axios
          .get('/api-v1/materials/?op='+'create'+txt)
          .then((response) => {
            this.$set(this.disp_list, 'create_materials', response.data)
          })
          .catch(function(error) {
              console.log(error)
          })
      }else{
        this.axios
          .get('/api-v1/materials/?op='+'evaluation')
          .then((response) => {
            this.$set(this.disp_list, 'eval_materials', response.data)
          })
          .catch(function(error) {
              console.log(error)
          })
        this.axios
          .get('/api-v1/materials/?op='+'create')
          .then((response) => {
            this.$set(this.disp_list, 'create_materials', response.data)
          })
          .catch(function(error) {
              console.log(error)
          })
      }
    },
    touchToMaterial(cid, bid, vid){
      console.log(cid)
      console.log(bid)
      console.log(vid)
      this.$router.push({ name: 'MaterialsDescription', params: {cid: cid, bid: bid, vid: vid}})
    },
  },
};
</script>

